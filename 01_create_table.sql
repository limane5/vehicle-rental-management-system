 CREATE TABLE AUDIT_LOG (
    AUDIT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TABLE_NAME VARCHAR2(50),
    OPERATION VARCHAR2(20),
    USER_NAME VARCHAR2(50),
    ACTION_DATE DATE
);
 CREATE OR REPLACE TRIGGER TRG_AUDIT_CUSTOMERS
AFTER INSERT OR UPDATE OR DELETE
ON CUSTOMERS
FOR EACH ROW
BEGIN
    INSERT INTO AUDIT_LOG
    (
        TABLE_NAME,
        OPERATION,
        USER_NAME,
        ACTION_DATE
    )
    VALUES
    (
        'CUSTOMERS',
        CASE
            WHEN INSERTING THEN 'INSERT'
            WHEN UPDATING THEN 'UPDATE'
            WHEN DELETING THEN 'DELETE'
        END,
        USER,
        SYSDATE
    );
END;
/
 CREATE OR REPLACE TRIGGER TRG_AUDIT_CUSTOMERS
AFTER INSERT OR UPDATE OR DELETE
ON CUSTOMERS
FOR EACH ROW
DECLARE
    V_OPERATION VARCHAR2(20);
BEGIN
    IF INSERTING THEN
        V_OPERATION := 'INSERT';
    ELSIF UPDATING THEN
        V_OPERATION := 'UPDATE';
    ELSIF DELETING THEN
        V_OPERATION := 'DELETE';
    END IF;

    INSERT INTO AUDIT_LOG
    (
        TABLE_NAME,
        OPERATION,
        USER_NAME,
        ACTION_DATE
    )
    VALUES
    (
        'CUSTOMERS',
        V_OPERATION,
        USER,
        SYSDATE
    );
END;
